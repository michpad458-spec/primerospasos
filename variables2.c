#include <stdio.h>

int main() {
//declaracion de una variable tipo float (numero que puede tener decimales)
//definir el tipo de dato, nombre de la variable + el valor de la variable
//ENTRADA DE DATOS
    float altura = 1.75;//valor de la variable altura
    float peso = 70.5;// valor de la variable peso
//procesamiento de datos tratamientos de datos
    printf("La altura es: %f\n", altura);
    printf("El peso es: %f\n", peso);
    printf("La altura es: %f y el peso es: %f", altura, peso);
//salida de datos
    return 0;
}